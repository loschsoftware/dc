namespace Dassie.Configuration.Global;

/// <summary>
/// Represents the base type of a global configuration property.
/// </summary>
public enum GlobalConfigBaseType
{
    /// <summary>
    /// A boolean value.
    /// </summary>
    Boolean,
    /// <summary>
    /// A string value.
    /// </summary>
    String,
    /// <summary>
    /// An integral value.
    /// </summary>
    Integer,
    /// <summary>
    /// A real number.
    /// </summary>
    Real
}